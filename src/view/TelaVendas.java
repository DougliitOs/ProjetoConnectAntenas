    public void readJTableForDesc(String desc) {
        
        DefaultTableModel modelo = (DefaultTableModel) tabelaPesqProd.getModel();
        modelo.setNumRows(0);
        ProdutoDAO pdao = new ProdutoDAO();

        pdao.readForDesc(desc).stream().forEach((p) -> {
            modelo.addRow(new Object[]{
                p.getDescricao(),
                p.getPrecoVenda(),
                p.getQtd()
            });
        });

    }
    
