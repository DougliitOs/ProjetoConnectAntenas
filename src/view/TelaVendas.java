    private void addItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addItemActionPerformed

        conn.conexao();
        
        modvenda.setNomeProd(nomeProd.getText());
        modvenda.setQtdItemVenda(Integer.parseInt(qtdItem.getText()));
        modvenda.setIdVenda(codVenda);
        try {
            control.adicionaItem(modvenda);
            preencherTabelaItensVenda("select * from produto inner join itensdavenda on produto.id_produto = itensdavenda.id_produto " +
            "inner join venda on venda.id_venda = itensdavenda.id_venda where venda.id_venda ="+codVenda);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao adicionar o item!");
        }
        
    }//GEN-LAST:event_addItemActionPerformed

