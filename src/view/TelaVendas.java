    private void buscaProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscaProdActionPerformed
         conn.conexao();
        
        try{
             PreparedStatement pst = conn.conn.prepareStatement("insert into venda (valor_venda) values(?)");
             pst.setFloat(1,0);
             pst.execute(); //insere uma nova venda no banco de dados
             conn.executaSQL("select * from venda");
             conn.rs.last();
             codVenda = conn.rs.getInt("id_venda");
        }catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "ERRO: " + ex);
        }
        readJTableForDesc(nomeProd.getText());
    }//GEN-LAST:event_buscaProdActionPerformed

